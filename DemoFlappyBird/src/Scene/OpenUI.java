    public void isGameover(int score) {
        GameOverDialog gameOverDialog = new GameOverDialog(this, score);
        gameOverDialog.setVisible(true);
        // Stop listening to key press
        scenePanel.removeKeyListener(gameScene);
        this.saveBtn.setEnabled(false);

        // Waiting for user to close dialog
        gameOverDialog.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {

                //Remove score of last game
                scoreLabel.setText("  ");
                gameScene = null;

                //prepare for the next game
                saveBtn.setEnabled(true);
                playBtn.setText("Play");
                isGameplaying = false;
                showGameInfo();
            }
        });
    }

    private void createGameScene() {
        this.gameScene = new GameScene(scoreLabel);

        this.gameScene.setPreferredSize(this.scenePanel.getSize());
        this.scenePanel.setLayout(new CardLayout());
        this.scenePanel.removeAll();
        this.scenePanel.add(gameScene);
        this.scenePanel.addKeyListener(gameScene);
        this.scenePanel.requestFocus();
        this.revalidate();
        this.repaint();

        if (!saveGameManagement()) {
            gameScene.addCharacter();
        }
        gameScene.setScorePanel(scoreLabel);

    }

    private boolean saveGameManagement() {
        if (!gameScene.isSavedGame()) {
            return false;
        }

        if (JOptionPane.showConfirmDialog(null, "Do you want to resume your last run?", "Save game",
                JOptionPane.YES_NO_OPTION) == 1) {
            return false;
        }

        if (!gameScene.recoverSave()) {
            JOptionPane.showMessageDialog(null, "Can not recover previous game!");
            return false;
        }
        return true;
    }

    private void showGameInfo() {
        this.scenePanel.removeAll();
        this.scenePanel.add(new GameInfoScene(this.scenePanel.getSize()));
        this.revalidate();
        this.repaint();
    }

