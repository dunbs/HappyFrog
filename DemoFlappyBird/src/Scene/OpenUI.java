    private void exitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exitBtnActionPerformed
        if (isGameplaying) {
            saveBtnActionPerformed(null);
        }
        System.exit(0);
    }//GEN-LAST:event_exitBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (gameScene != null) {
            gameScene.saveGame();
        } else {
            JOptionPane.showMessageDialog(null, "You must start the game to save!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void playBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playBtnActionPerformed
        if (!isGameplaying) {
            this.createGameScene();
            this.playBtn.setText("Pause");
            isGameplaying = true;
            return;
        }

        if (playBtn.getText().equals("Pause")) {
            gameScene.pause();
            playBtn.setText("Resume");
            return;
        }
        if (playBtn.getText().equals("Resume")) {
            gameScene.resume();
            playBtn.setText("Pause");
        }
    }//GEN-LAST:event_playBtnActionPerformed

